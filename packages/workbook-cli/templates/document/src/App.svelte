  <h1>%%NAME%%</h1>
  <p class="meta">A workbook document. Edit src/App.svelte to start writing.</p>

  <p>
    Documents are workbooks shaped like long-form reading: a single
    column of prose, light chrome, fixed line measure. Run
    <code>workbook build</code> to compile to one self-contained
    <code>.html</code> file — email it, host it, embed it.
  </p>

  <hr />

  <h2>Add interactivity</h2>
  <p>
    Drop Svelte components into this file or import them from
    <code>./src/</code>. Reactive state, charts, forms — anything you
    write here ships inside the artifact. If you import any
    <code>wb.*</code> WASM APIs, change <code>wasmVariant</code> in
    <code>workbook.config.mjs</code> from <code>"none"</code> to
    <code>"default"</code>.
  </p>
